import fs from "node:fs";
import path from "node:path";

import { appRoot } from "./resolve-dist-dir.mjs";

const REPO_ROOT = path.resolve(appRoot, "..");
const CHANGELOG_FILE = path.join(REPO_ROOT, "CHANGELOG.md");
const OUTPUT_FILE = path.join(
  appRoot,
  "src/lib/changelog/changelog-content.generated.ts",
);

function assertReadableChangelogPath() {
  if (path.basename(CHANGELOG_FILE) !== "CHANGELOG.md") {
    throw new Error("changelog path must target CHANGELOG.md");
  }

  let canonical;
  try {
    canonical = fs.realpathSync(CHANGELOG_FILE);
  } catch {
    throw new Error("changelog path is not readable");
  }

  const repoRoot = fs.realpathSync(REPO_ROOT);
  const relative = path.relative(repoRoot, canonical);
  if (relative.startsWith("..") || path.isAbsolute(relative)) {
    throw new Error("changelog path must stay within the repository root");
  }

  return canonical;
}

function buildGeneratedModule(content) {
  return `/** DO NOT EDIT — generated by web/scripts/generate-changelog-content.mjs */
export const CHANGELOG_MARKDOWN: string = ${JSON.stringify(content)};
`;
}

function main() {
  const resolved = assertReadableChangelogPath();
  const content = fs.readFileSync(resolved, "utf8");
  fs.mkdirSync(path.dirname(OUTPUT_FILE), { recursive: true });
  fs.writeFileSync(OUTPUT_FILE, buildGeneratedModule(content), "utf8");
}

main();
